namespace graphs.models {
    export class Size {
        private _width: number;
        public get width(): number {
            return this._width;
        }
        public set width(v: number) {
            this._width = v;
        }

        private _height: number;
        public get height(): number {
            return this._height;
        }
        public set height(v: number) {
            this._height = v;
        }







    }

}